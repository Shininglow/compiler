	<h3 id="easy-twitter-feed-widget"><?php _e( 'Easy Twitter Feed' ); ?></h3>

<p>This <a href="https://wordpress.org/plugins/easy-twitter-feed-widget/" target="_blank">Easy Twitter Feed Widget plugin</a> is designed to display tweets on your site without creating an API.</p>
<p>
Using Easy Twitter Feed Plugin twitter widget publish your twitter post & show your twitter page fans. </p>


<h4>Installing Easy Twitter Feed plugin</h4>

<ol class="index-list">
	<li><p> Upload the easy-twitter-feed-widget folder to the /wp-content/plugins/ directory</p></li>
	<li><p> Activate the Easy Twitter Feed  plugin through the 'Plugins' menu in WordPress</p></li>
	<li><p> Configure the plugin by going to the Settings > Easy Twitter Feed  Options </p></li>
	<li><p> Use the Easy Twitter Feed  by going to the Appearance > Widgets</p></li>
</ol>
<p><strong>Next you need to get Twitter Widget ID. Follow the steps listed below: </strong></p>
<ol class="index-list">
	<li><p> Sign In to your Twitter Account</p></li>
	<li><p> Go to Settings > Widgets > Create new</p></li>
	<li><p> Click Create Widget button</p></li>
	<li><p> You can copy the Widget ID from Browser Address Bar e.g. 419173025219799552</p></li>
	<li><p> OR You can copy the Widget ID from the given HTML under the Widget Preview i.e. data-widget-id="419173025219799552"</p></li>
</ol>


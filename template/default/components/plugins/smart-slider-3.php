<p>This <a href="https://wordpress.org/plugins/smart-slider-3/" target="_blank">Smart Slider 3</a> plugin is all you need for creating beautiful slideshows, YouTube and Vimeo slides, with smooth transitions, effects and animations. It creates slides from your WordPress posts, even more it has a complete easy-to-use slide editor with different layers.</p>

<h4>Automatic Installation</h4>

<ol class="index-list">
<p><li>search for Smart Slider 3 through <strong>Plugins > Add New</strong> interface;</li></p>
<p><li>find the plugin box of Smart Slider 3 and click on the <strong>Install Now</strong> button;</li></p>
<p><li>activate the Smart Slider 3 plugin;</li></p>
<p><li>create a new slider: Click on the <strong>Smart Slider</strong> menu, click on the create slider button. Then you can add images and videos as slide and you can customize your slides with layers;</li></p>
<p><li>copy the shortcode from the Publish slider box and paste it into a post or a page.</li></p></ol>

<h4>Manual Installation</h4>

<ol class="index-list">
<p><li>download Smart Slider 3;</li></p>
<p><li>upload the Smart Slider 3 through <strong>Plugins > Add New > Upload</strong> interface or upload smart-slider-3 folder to the <strong>wp-content/plugins</strong> directory;</li></p>
<p><li>activate the Smart Slider 3 plugin through the <strong>Plugins</strong> menu in WordPress;</li></p>
<p><li>create a new slider: Click on the <strong>Smart Slider</strong> menu, click on the create slider button. Now you can add images and videos as slide and you can customize your slides with layers.</li></p></ol>

<p>Detailed documentation about Smart Slider 3 can be found <a href="https://smartslider3.helpscoutdocs.com/" target="_blank">here</a>.</p>

<h2 id="woocommerce-settings">WooCommerce Settings</h2>

<p>Whenever you need to set your shop base location, shipping location, enable taxes, manage checkout and default shop pages, etc., you need to access WooCommerce settings. </p>

<h4>WooCommerce Settings Location</h4>

<ol class="index-list">
<li>Log in to WordPress Dashboard. Then, navigate to “Woocommerce” tab on the left.</li>
<li>Open “Settings” sub menu. Here you’ll see the settings tabs at the top of the page. </li></ol>

<h4>Settings Overview </h4>

<ul class="marked-list">
<li><p><b>General</b> — in this block you can manage your shop base location, selling location, shipping location, enable taxes, add your custom store notice text, etc. You can as well manage currency options.</p></li>

<li><p><b>Products</b> — here you can access the general, display, inventory and downloadable products settings.

<h6>General </h6>

<ul class="marked-list">
<li><b>Measurements</b> — here you can define the weight unit, dimensions unit.</li>
<li><b>Reviews</b> — here you can enable and disable reviews and product ratings.</li>
<li><b>Advanced Thumbnails</b> — here you can manage product thumbnails. </li>
<h6>Display</h6>
<li><b>Shop and product pages</b> — define the pages to display as the shop and product ones, select the type of the product sorting, set the default category display, etc. </li>
<li><b>Product images</b> — here you can set the dimensions for the images in the products catalogue, image thumbnails dimensions, etc. </li>
<h6>Inventory</h6>
<li><b>Inventory</b> — here you can manage your store stock, hold stock, use notifications, etc. </li>
<h6>Downloadable products </h6>
<li><b>Downloadable products</b> — here you can define the file download method, set the access restriction, etc. </li></ul>

</p></li>

<li><b>Tax</b> — in this block you can manage tax options, set the tax standard, reduced and zero rates. </li>
<li><b>Shipping</b> — here you can add shipping zones and manage shipping options, such as shipping destination, calculations, etc. </li>
<li><b>Checkout</b> — in this block one can create coupons, enable them, define the checkout process, set the checkout pages, such as Cart page, Checkout page, etc. </li>
<li><b>Accounts</b> — here you can set the customer registration page, enable automatic login and password generator, etc. </li>
<li><b>Emails</b> — here you can manage email sender options, customize email templates, etc. </li></ul>


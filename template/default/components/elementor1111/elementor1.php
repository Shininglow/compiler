<h3>Elementor</h3>

<h5>
<p><a href="https://docs.elementor.com/" target="_blank">Elementor</a> is a plugin that can be used for creating pages and building pages’ structure. You can also add all kinds of content using Elementor addons. </p>
</h5>

<h5><strong>Switching to Elementor</strong></h5>

<ol class="index-list">
<li><p>Open pages tab in Administration Panel. Click on Add New to open the standard editing form.</p>


<?php assets_manager()->get_image( 'assets/images/elementor1.png', 'img-preview', _('Insert Columns dialog') ); ?>



</li>

<li><p>Click on Edit with Elementor button.</p></li>
</ol>

<h5><strong>Creating Page Structure</strong></h5>

<ol class="index-list">
<li><p>Click on Add New Section button to add a new section to the new page.</p>

<?php assets_manager()->get_image( 'assets/images/elementor2.png', 'img-preview', _('Insert Columns dialog') ); ?>
</li>

<li><p>Choose the structure you need to use from the newly opened form. You can select the number of columns you want to add to the section.</p></li>

<li><p>Drag and drop the element you want to add to the column area.</p></li>

<li><p>Customize the element, add content to it to view the result in real time on the right side of the screen, where your new page is displayed.</p>

<?php assets_manager()->get_image( 'assets/images/elementor3.png', 'img-preview', _('Insert Columns dialog') ); ?>

</li>

<li><p>When you’re ready, click on Save button.</p></li>
</ol>
